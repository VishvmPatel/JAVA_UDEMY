public class ArithmeticOperationsForDifferentDataTypes {
    public static void main(String[] args) {
        byte a=10;
        short b=15;
        int c=a+b;

        float d=12.5f;
        long e=1231;
        float f=d*e;

        float g=12.5f;
        double h=123;
        double i=g*h;

        char j=40;
        int k=30;
        int l=j-k;
        System.out.println(c);
        System.out.println(f);
        System.out.println(i);
        System.out.println(l);
    }
}
