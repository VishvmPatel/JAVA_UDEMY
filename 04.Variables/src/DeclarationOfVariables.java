import java.lang.*;
public class DeclarationOfVariables {
    public static void main(String[] args) {
        byte b = 5; //if we don't give a value to b we will get an error as a variable has to be initialized.
        System.out.println(b);
        b=25; //value of the variable has been changed.
        System.out.println(b);
    }
}
